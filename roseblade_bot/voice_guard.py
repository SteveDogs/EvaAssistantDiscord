from __future__ import annotations

from dataclasses import dataclass
import random


@dataclass(frozen=True, slots=True)
class VoiceGuardPack:
    owner_lines: tuple[str, ...]
    protected_lines: tuple[str, ...]

    @property
    def variants_count(self) -> int:
        return len(self.owner_lines) + len(self.protected_lines)

    def render_warning(self, *, target_name: str, is_owner_target: bool) -> str:
        pool = self.owner_lines if is_owner_target else self.protected_lines
        template = random.choice(pool or self.owner_lines or self.protected_lines)
        return template.format(target=target_name).strip()


