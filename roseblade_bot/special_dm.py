from __future__ import annotations

from dataclasses import dataclass
import random


def _cap_first(text: str) -> str:
    cleaned = text.strip()
    if not cleaned:
        return cleaned
    return cleaned[:1].upper() + cleaned[1:]


@dataclass(frozen=True, slots=True)
class SpecialDmEventPack:
    direct_lines: tuple[str, ...]
    openings: tuple[str, ...]
    reactions: tuple[str, ...]
    closings: tuple[str, ...]

    def render(self, **context: str) -> str:
        # Prefer short direct phrases so EVA sounds closer to a real message.
        if self.direct_lines and random.random() < 0.9:
            return random.choice(self.direct_lines).format(**context).strip()

        opening = random.choice(self.openings).format(**context).strip()
        reaction = random.choice(self.reactions).format(**context).strip()
        closing = random.choice(self.closings).format(**context).strip()

        pattern = random.choice(
            (
                "{opening}, {reaction}.",
                "{opening}, {reaction}. {closing}",
            )
        )
        return pattern.format(
            opening=opening,
            reaction=reaction,
            reaction_cap=_cap_first(reaction),
            closing=closing,
        ).strip()


@dataclass(frozen=True, slots=True)
class SpecialDmPack:
    voice_join: SpecialDmEventPack
    avatar_changed: SpecialDmEventPack

    def render_voice_join(self, *, channel_name: str) -> str:
        return self.voice_join.render(channel=channel_name)

    def render_avatar_changed(self) -> str:
        return self.avatar_changed.render()


